const mongoose = require("mongoose");

const registerSchema = new mongoose.Schema({
  fullname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  confirmpassword: {
    type: String,
    required: true,
  },
});

// create collection

const Register = mongoose.model("Register", registerSchema);

module.exports = Register;
